import java.util.*;
class Type_casting
{
    public static void main(String args[])
    {
        byte b=10;
        int a=b;
        System.out.println(b);
        System.out.println(a);
    
        float f=22.14f;
        double d=f;
        System.out.println(f);
        System.out.println(d);

        double e=10.5265777676d;
        long l=(long)e;
        System.out.println(e);
        System.out.println(l);
    }
}